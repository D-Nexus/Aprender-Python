numero1 = input("Ingrese el primer numero: ") #Ingresamos un número con "input"
numero2 = input("Ingrese el segundo numero: ")
numero1 = int(numero1) #Debemos convertir los numeros a numeros enteros con la función "int"
numero2 = int(numero2)
resultado = numero1 + numero2 #Sumamos los resultados como numero enteros
print("Su resultado es: ", resultado) #Imprimimos el resultado de la suma con "print"